let currentLang = localStorage.getItem('lang') || 'en';

/* ====== Language Switch ====== */
function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);

    // Update all i18n elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const translation = i18n[lang][key];
        if (translation) {
            el.textContent = translation;
        }
    });

    // Update language toggle label
    const langLabel = document.getElementById('langLabel');
    if (langLabel) {
        langLabel.textContent = lang === 'en' ? 'VI' : 'EN';
    }

    // Update HTML lang attribute
    document.documentElement.lang = lang;

    // Update placeholder images if on detail pages
    updateDetailPlaceholders(lang);

    // Toggle language-specific content blocks
    document.querySelectorAll('.lang-content').forEach(el => {
        const contentLang = el.getAttribute('lang');
        el.style.display = contentLang === lang ? 'block' : 'none';
    });
}

function toggleLanguage() {
    const newLang = currentLang === 'en' ? 'vi' : 'en';
    setLanguage(newLang);
}

function updateDetailPlaceholders(lang) {
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        const translation = i18n[lang][key];
        if (translation) {
            el.textContent = translation;
        }
    });
}

/* ====== Navbar Scroll Effect ====== */
window.addEventListener('scroll', () => {
    const navbar = document.getElementById('navbar');
    if (window.scrollY > 50) {
        navbar.classList.add('scrolled');
    } else {
        navbar.classList.remove('scrolled');
    }

    // Scroll to top button
    const scrollTop = document.getElementById('scrollTop');
    if (window.scrollY > 400) {
        scrollTop.classList.add('visible');
    } else {
        scrollTop.classList.remove('visible');
    }
});

/* ====== Mobile Menu Toggle ====== */
const menuToggle = document.getElementById('menuToggle');
const navLinks = document.getElementById('navLinks');
const navBackdrop = document.getElementById('navBackdrop');

if (menuToggle && navLinks && navBackdrop) {
    function openMenu() {
        menuToggle.classList.add('active');
        navLinks.classList.add('active');
        navBackdrop.classList.add('active');
        document.body.style.overflow = 'hidden';
    }

    function closeMenu() {
        menuToggle.classList.remove('active');
        navLinks.classList.remove('active');
        navBackdrop.classList.remove('active');
        document.body.style.overflow = '';
    }

    menuToggle.addEventListener('click', () => {
        if (navLinks.classList.contains('active')) {
            closeMenu();
        } else {
            openMenu();
        }
    });

    navBackdrop.addEventListener('click', closeMenu);

    // Close menu on link click (mobile)
    navLinks.querySelectorAll('a').forEach(link => {
        link.addEventListener('click', closeMenu);
    });
}

/* ====== Language Toggle ====== */
const langToggle = document.getElementById('langToggle');
if (langToggle) {
    langToggle.addEventListener('click', toggleLanguage);
}

/* ====== Theme Toggle ====== */
let currentTheme = localStorage.getItem('theme') || 'dark';

function setTheme(theme) {
    currentTheme = theme;
    localStorage.setItem('theme', theme);
    document.documentElement.setAttribute('data-theme', theme);
    
    const themeIcon = document.querySelector('#themeToggle i');
    if (themeIcon) {
        themeIcon.className = theme === 'dark' ? 'fas fa-moon' : 'fas fa-sun';
    }
}

function toggleTheme() {
    const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
    setTheme(newTheme);
}

const themeToggle = document.getElementById('themeToggle');
if (themeToggle) {
    themeToggle.addEventListener('click', toggleTheme);
}

/* ====== Scroll to Top ====== */
const scrollTop = document.getElementById('scrollTop');
if (scrollTop) {
    scrollTop.addEventListener('click', () => {
        window.scrollTo({ top: 0, behavior: 'smooth' });
    });
}

/* ====== Smooth Scroll for Nav Links ====== */
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function(e) {
        const href = this.getAttribute('href');
        if (href === '#') return;
        e.preventDefault();
        const target = document.querySelector(href);
        if (target) {
            target.scrollIntoView({ behavior: 'smooth' });
        }
    });
});

/* ====== Keyboard Shortcut: Ctrl+Shift+L to toggle language ====== */
document.addEventListener('keydown', (e) => {
    if (e.ctrlKey && e.shiftKey && e.key === 'L') {
        e.preventDefault();
        toggleLanguage();
    }
});

/* ====== Initialize on page load ====== */
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLang);
    setTheme(currentTheme);
});
